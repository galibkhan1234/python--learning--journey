name = "I am GalibKhan"

print(name)  # Output: I am GalibKhan
print(len(name))  # Output: 15
print(name.startswith("I am"))  # Output: True
print(name.endswith("Khan"))  # Output: True
print(name.find("Galib"))  # Output: 5
print(name.replace("Galib", "Ali"))  # Output: I am AliKhan
print(name.upper())  # Output: I AM GALIBKHAN
print(name.lower())  # Output: i am galibkhan
print(name.title())  # Output: I Am Galibkhan
print(name.split())  # Output: ['I', 'am', 'GalibKhan']
print(name.count("am")) # Output: 1 
print(name.count("a"))  # Output: 3